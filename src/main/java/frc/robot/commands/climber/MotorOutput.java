package frc.robot.commands.climber;

import edu.wpi.first.wpilibj.command.Command;
import edu.wpi.first.wpilibj.command.Subsystem;
import edu.wpi.first.wpilibj.smartdashboard.SmartDashboard;
import frc.robot.subsystems.climber.Climber;

public class MotorOutput extends Command {
  private Climber climberSubsystem;
  private String climberString;

  public MotorOutput(Climber subsystem, String prefix) {
    
    climberSubsystem = subsystem;
    
    // Use requires() here to declare subsystem dependencies
    // eg. requires(chassis);
  }

  // Called just before this Command runs the first time
  @Override
  protected void initialize() {
  }

  // Called repeatedly when this Command is scheduled to run
  @Override
  protected void execute() {
    double Current = climberSubsystem.motorCurrent();
    double Voltage = climberSubsystem.motorVoltage();
    SmartDashboard.putNumber(climberString + " Motor Current", Current);
    SmartDashboard.putNumber(climberString + "Motor Voltage", Voltage);
    
  }

  // Make this return true when this Command no longer needs to run execute()
  @Override
  protected boolean isFinished() {
    return false;
  }

  // Called once after isFinished returns true
  @Override
  protected void end() {
  }

  // Called when another command which requires one or more of the same
  // subsystems is scheduled to run
  @Override
  protected void interrupted() {
  }
}
